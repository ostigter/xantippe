package xantippe;


/**
 * Supported media types for resources.
 * 
 * @author Oscar Stigter
 */
public enum MediaType {
    
    /** XML document. */
    XML,
    
    /** XML schema. */
    SCHEMA,
    
    /** XQuery module. */
    XQUERY,
    
    /** Plain text resource. */
    PLAIN_TEXT,
    
    /** Binary resource. */
    BINARY,

}
